package Java;

public class ArraysAndLoops {
    public static void main(String[] args) {
        // Arrays
        String[] fruits = {"apple", "banana", "orange"};

        // Loop through the array
        for (String fruit : fruits) {
            System.out.println(fruit);
        }

        // Loop using for loop
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
    }
}